// Database configuration
$servername = "127.0.0.1";
$username = "root";
$password = "";
$dbname = "ecoFarmingDB";

// Create a connection to the database
$conn = mysqli_connect($servername, $username, $password, $dbname);

// Check if the connection was successful
if (!$conn) {
    die("Connection failed: " . mysqli_connect_error());
}

$sql = "SELECT queries.name AS asker, queries.question, replies.reply
        FROM queries
        LEFT JOIN replies ON queries.question = replies.question_identifier";
$result = mysqli_query($conn, $sql);

if ($result->num_rows > 0) {
    while ($row = $result->fetch_assoc()) {
        // Display each question and answer in a container
        echo "<section class='blog-content3'>";
        echo "<div class='resourceBox3' id='boxText'>";
        echo "<h1>Question asked by : " . $row["asker"] . "</h1><br>";
        echo "<h3>Question: </h3>" . "<p>" . $row["question"] . "</p>";
        echo "<h3>Solution/advice: </h3>". "<p>" . $row["reply"] . "</p>";
        echo "</div>";
        echo "</section>";
    }
} else {
    echo "No queries found.";
}

mysqli_close($conn);
