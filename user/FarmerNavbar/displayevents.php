<?php
// Database configuration
$servername = "127.0.0.1";
$username = "root";
$password = "";
$dbname = "ecoFarmingDB";

// Create a connection to the database
$conn = mysqli_connect($servername, $username, $password, $dbname);

// Check if the connection was successful
if (!$conn) {
    die("Connection failed: " . mysqli_connect_error());
}

// Query to retrieve event data
$sql = "SELECT * FROM events";
$result = mysqli_query($conn, $sql);

// Check if there are any results
if (mysqli_num_rows($result) > 0) {
	
    // Loop through the results and create event containers
    while ($row = mysqli_fetch_assoc($result)) {
    	echo '<section class="blog-content margin1">';
        echo '<div class="resourceBox2">';
	    echo '<h1>' . $row['eventName'] . '</h1>';
	    echo '<h1>' . $row['eventDate'] . '</h1>';
	    echo '<div class="row">';
	    echo '<div class="about-col">';
	    echo '<p>' . $row['eventDescription'] . '</p>';
	    echo '</div>';
	    echo '<div class="about-col" style="margin-top: -40px;">';

	    // Display the event image if it exists
if (!empty($row['eventImage'])) {
    echo '<img src="' . $row['eventImage'] . '" alt="' . $row['eventName'] . '">';
}

    echo '</div>';
    echo '</div>';
    echo '<a href="http://localhost/st20253489-ecofarming/Applyform/applicationForm.php" class="buttonClass2">Click here to register for our event</a>';
    echo '</div>';
    echo '</section>';
    }

} else {
    echo "No queries found.";
}

mysqli_close($conn);
