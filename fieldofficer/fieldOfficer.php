			<h2>Why should we participate in ecological farming?</h2><br>

			<p>The problem we face currently is not with producing more food, but rather with producing it in a way that respects nature and goes where it is most needed. This is not accomplished by the current industrial agriculture system.

			The environment is suffering a lot in the meantime. As a result of our excessive resource use, soil fertility, biodiversity, and water quality are declining. <br> Our surrounding environment is becoming more and more toxic. Waste production is increasing.
			</p>
