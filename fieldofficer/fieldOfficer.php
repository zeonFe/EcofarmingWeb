<p>
All of this is taking place as the Earth's resources are under increasing pressure due to climate change.<br>
Our existing agricultural system relies heavily on the usage of both fossil fuels and chemicals. And is governed by a large group of powerful corporations that are concentrated in a small number of wealthy industrialized nations. This system heavily relies on a select few important crops, eroding the foundation of the ecological and food systems that support human life. Water, land, and air are all contaminated and toxicated by this agriculture system. It hurts biodiversity, farmers' livelihoods, and consumer welfare in addition to making a significant contribution to climate change. Which is why we need to start making a change.</p>

