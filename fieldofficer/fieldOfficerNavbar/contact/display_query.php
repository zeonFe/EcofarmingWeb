    die("Connection failed: " . mysqli_connect_error());
}

$sql = "SELECT * FROM queries";
$result = $conn->query($sql);

if ($result->num_rows > 0) {
    while ($row = $result->fetch_assoc()) {

        // Display each question inside the .resourceBox3 container
        echo "<section class='blog-content3'>";
        echo "<div class='resourceBox3'>";
        echo "<h2>Question from: " . $row["name"] . "</h2><br>";
        echo "<h2>Category of issue: " . $row["category"] . "</h2><br>";
        echo "<p>" . $row["question"] . "</p>";
        echo "<a href='http://localhost/st20253489-ecofarming/contact/queryReplyForm.php?question=" . urlencode($row["question"]) . "&name=" . urlencode($row["name"]) . "&category=" . urlencode($row["category"]) . "' class='buttonClass4'>Click here to reply</a>";
